def _first_friday(year: int, month: int) -> datetime:
    """Return the first Friday of a given month (NFP release day)."""
    d = datetime(year, month, 1)
    days_until_friday = (4 - d.weekday()) % 7
    return d + timedelta(days=days_until_friday)


def _hardcoded_events(start: datetime, end: datetime) -> list[dict]:
    """Generate known high-impact USD events between start and end."""
    events = []
    dt = start.replace(tzinfo=None) if start.tzinfo else start
    end_dt = end.replace(tzinfo=None) if end.tzinfo else end

    # FOMC — 14:00 ET = 19:00 UTC
    for d in _FOMC_DATES:
        ts = datetime.strptime(d, "%Y-%m-%d").replace(hour=19, minute=0)
        if dt <= ts <= end_dt:
            events.append({"title": "FOMC Statement", "impact": "High",
                           "country": "USD", "ts": ts})

    # CPI — 08:30 ET = 13:30 UTC
    for d in _CPI_DATES:
        ts = datetime.strptime(d, "%Y-%m-%d").replace(hour=13, minute=30)
        if dt <= ts <= end_dt:
            events.append({"title": "CPI y/y", "impact": "High",
                           "country": "USD", "ts": ts})

    # PPI — 08:30 ET = 13:30 UTC
    for d in _PPI_DATES:
        ts = datetime.strptime(d, "%Y-%m-%d").replace(hour=13, minute=30)
        if dt <= ts <= end_dt:
            events.append({"title": "PPI m/m", "impact": "High",
                           "country": "USD", "ts": ts})

    # Retail Sales — 08:30 ET = 13:30 UTC
    for d in _RETAIL_SALES_DATES:
        ts = datetime.strptime(d, "%Y-%m-%d").replace(hour=13, minute=30)
        if dt <= ts <= end_dt:
            events.append({"title": "Retail Sales m/m", "impact": "High",
                           "country": "USD", "ts": ts})

    # GDP Advance — 08:30 ET = 13:30 UTC
    for d in _GDP_DATES:
        ts = datetime.strptime(d, "%Y-%m-%d").replace(hour=13, minute=30)
        if dt <= ts <= end_dt:
            events.append({"title": "GDP q/q Advance", "impact": "High",
                           "country": "USD", "ts": ts})

    # ISM Manufacturing PMI — 10:00 ET = 15:00 UTC
    for d in _ISM_MFG_DATES:
        ts = datetime.strptime(d, "%Y-%m-%d").replace(hour=15, minute=0)
        if dt <= ts <= end_dt:
            events.append({"title": "ISM Manufacturing PMI", "impact": "High",
                           "country": "USD", "ts": ts})

    # ISM Services PMI — 10:00 ET = 15:00 UTC
    for d in _ISM_SVC_DATES:
        ts = datetime.strptime(d, "%Y-%m-%d").replace(hour=15, minute=0)
        if dt <= ts <= end_dt:
            events.append({"title": "ISM Services PMI", "impact": "High",
                           "country": "USD", "ts": ts})

    # NFP — first Friday of each month, 08:30 ET = 13:30 UTC
    cur = datetime(dt.year, 1, 1)
    while cur <= end_dt:
        ff = _first_friday(cur.year, cur.month).replace(hour=13, minute=30)
        if dt <= ff <= end_dt:
            events.append({"title": "Non-Farm Payrolls", "impact": "High",
                           "country": "USD", "ts": ff})
        if cur.month == 12:
            cur = datetime(cur.year + 1, 1, 1)
        else:
            cur = datetime(cur.year, cur.month + 1, 1)

    return events


def _fetch_week_from_api(monday: datetime) -> list[dict]:
    """Attempt to fetch FF calendar for the week starting on monday."""
    cache_file = _CACHE_DIR / f"ff_{monday.strftime('%Y%m%d')}.json"
    if cache_file.exists():
        with open(cache_file) as f:
            return json.load(f)

    today = datetime.utcnow().date()
    target = monday.date()

    if target > today:
        url = "https://nfs.faireconomy.media/ff_calendar_nextweek.json"
    elif abs((target - today).days) <= 7:
        url = "https://nfs.faireconomy.media/ff_calendar_thisweek.json"
    else:
        return []   # historical weeks not served by this endpoint

    try:
        req = urllib.request.Request(url, headers={"User-Agent": "Mozilla/5.0"})
        with urllib.request.urlopen(req, timeout=10) as r:
            data = json.loads(r.read())
        with open(cache_file, "w") as f:
            json.dump(data, f)
        return data
    except Exception:
        return []


def _parse_api_events(raw: list[dict]) -> list[dict]:
    """Parse FF API JSON into internal event dicts."""
    out = []
    for item in raw:
        if item.get("impact") != "High":
            continue
        if item.get("country") not in ("USD", "XAU"):
            continue
        try:
            ts = datetime.fromisoformat(item["date"]).astimezone(timezone.utc)
            ts = ts.replace(tzinfo=None)
            out.append({"title": item["title"], "impact": "High",
                        "country": item["country"], "ts": ts})
        except Exception:
            continue
    return out


def get_high_impact_events(start: pd.Timestamp, end: pd.Timestamp) -> pd.DatetimeIndex:
    """
    Return timestamps of all high-impact USD news events between start and end.
    Combines live FF API (current/next week) with hardcoded historical fallback.
    """
    start_dt = pd.Timestamp(start).to_pydatetime().replace(tzinfo=None)
    end_dt   = pd.Timestamp(end).to_pydatetime().replace(tzinfo=None)

    # Try FF API for current/next week
    api_events: list[dict] = []
    monday = datetime.utcnow() - timedelta(days=datetime.utcnow().weekday())
    monday = monday.replace(hour=0, minute=0, second=0, microsecond=0)
    for offset in (0, 7):
        raw = _fetch_week_from_api(monday + timedelta(days=offset))
        api_events.extend(_parse_api_events(raw))

    # Hardcoded historical events
    fallback = _hardcoded_events(start_dt, end_dt)

    all_events = fallback + [e for e in api_events
                             if start_dt <= e["ts"] <= end_dt]

    if not all_events:
        return pd.DatetimeIndex([])

    timestamps = sorted(set(e["ts"] for e in all_events))
    return pd.DatetimeIndex(timestamps)


def mark_news_windows(
    df: pd.DataFrame,
    events: pd.DatetimeIndex,
    pre_minutes: int = 10,
    post_hours: int = 2,
) -> pd.DataFrame:
    """
    Add two boolean columns to df:
      news_avoid  — bars within pre_minutes BEFORE a high-impact event (stop hunts)
      news_entry  — intra-hour ICT kill zones (:50–:09) within post_hours after the event

    ICT principle: news displacement creates FVG/OB. The highest-probability entry is
    at the end-of-hour / start-of-hour window (:50→:09) where institutional order flow
    resumes — e.g. news at 13:30 → key windows are 13:50–14:09 and 14:50–15:09.
    """
    df = df.copy()
    df["news_avoid"] = False
    df["news_entry"] = False

    # Intra-hour kill zone minutes: last 10 of any hour + first 10 of next
    _kill_minutes = set(range(50, 60)) | set(range(0, 10))

    for ev_ts in events:
        # Avoid window: 10 min before news (stop-hunt phase)
        avoid_start = ev_ts - timedelta(minutes=pre_minutes)
        df.loc[(df.index >= avoid_start) & (df.index < ev_ts), "news_avoid"] = True

        # Entry window: intra-hour :50–:09 bars within post_hours after news
        entry_end = ev_ts + timedelta(hours=post_hours)
        mask = (
            (df.index >= ev_ts)
            & (df.index <= entry_end)
            & (df.index.minute.isin(_kill_minutes))
        )
        df.loc[mask, "news_entry"] = True

    return df
